/**
 * blog-links-report.mjs — derives the internal link graph of The Awakening
 * from the actual MDX files, so link tracking never rots.
 *
 * Usage:
 *   node scripts/blog-links-report.mjs          → print report to stdout
 *   node scripts/blog-links-report.mjs --write  → also refresh the generated
 *     section in agent-guides/blog/TOPIC_LEDGER.md
 *
 * Reports per post: outbound internal links (to siblings, categories, site),
 * inbound links from siblings, and flags:
 *   ORPHAN        — zero inbound links from sibling posts
 *   LOW-OUTBOUND  — fewer than 2 sibling links (violates SEO_GUIDE minimum)
 *   BROKEN        — link to a slug/category that does not exist
 *   NOT-IN-LEDGER — post file exists but has no row in TOPIC_LEDGER.md
 */

import fs from "node:fs";
import path from "node:path";

const ROOT = process.cwd();
const CONTENT_DIR = path.join(ROOT, "content", "awakening");
const LEDGER = path.join(ROOT, "agent-guides", "blog", "TOPIC_LEDGER.md");

const CATEGORY_PREFIXES = ["industry", "for", "type"];
// Kept in sync with src/lib/blog/taxonomy.js — update together.
const VALID_CATEGORIES = {
  industry: ["business", "faith", "government", "creators", "healthcare", "education", "nonprofit", "finance"],
  for: ["chiefs", "leaders-of-leaders", "emerging-leaders"],
  type: ["insight", "guide", "value"],
};

function extractFrontmatterField(raw, field) {
  const m = raw.match(new RegExp(`^${field}:\\s*(.+)$`, "m"));
  return m ? m[1].trim().replace(/^["']|["']$/g, "") : null;
}

const files = fs
  .readdirSync(CONTENT_DIR)
  .filter((f) => f.endsWith(".mdx") || f.endsWith(".md"));

const posts = files.map((filename) => {
  const raw = fs.readFileSync(path.join(CONTENT_DIR, filename), "utf8");
  const body = raw.replace(/^---[\s\S]*?---/, "");
  const slug =
    extractFrontmatterField(raw, "slug") ?? filename.replace(/\.mdx?$/, "");
  const draft = extractFrontmatterField(raw, "draft") === "true";

  const links = [...body.matchAll(/\]\((\/[^)\s]*)\)/g)].map((m) => m[1]);
  const postLinks = [];
  const categoryLinks = [];
  const siteLinks = [];
  const broken = [];

  for (const href of links) {
    const clean = href.replace(/[#?].*$/, "").replace(/\/$/, "");
    if (!clean.startsWith("/awakening")) {
      siteLinks.push(clean || "/");
      continue;
    }
    const rest = clean.replace(/^\/awakening\/?/, "");
    if (!rest) {
      siteLinks.push("/awakening");
      continue;
    }
    const [first, second] = rest.split("/");
    if (CATEGORY_PREFIXES.includes(first)) {
      if (VALID_CATEGORIES[first]?.includes(second)) categoryLinks.push(rest);
      else broken.push(clean);
    } else {
      postLinks.push(first);
    }
  }

  return { slug, filename, draft, postLinks, categoryLinks, siteLinks, broken };
});

const knownSlugs = new Set(posts.map((p) => p.slug));

// resolve broken post links + inbound counts
const inbound = new Map(posts.map((p) => [p.slug, []]));
for (const post of posts) {
  for (const target of post.postLinks) {
    if (!knownSlugs.has(target)) post.broken.push(`/awakening/${target}`);
    else inbound.get(target).push(post.slug);
  }
}

const ledgerRaw = fs.existsSync(LEDGER) ? fs.readFileSync(LEDGER, "utf8") : "";

const lines = [];
lines.push(`Posts scanned: ${posts.length}  (generated ${new Date().toISOString().slice(0, 10)})`);
lines.push("");
lines.push("| Post | Out → siblings | Out → categories | In ← siblings | Flags |");
lines.push("|------|----------------|------------------|---------------|-------|");

let problems = 0;
for (const post of posts.sort((a, b) => a.slug.localeCompare(b.slug))) {
  const inb = inbound.get(post.slug);
  const flags = [];
  if (!post.draft && inb.length === 0) flags.push("ORPHAN");
  const validSiblingLinks = post.postLinks.filter((t) => knownSlugs.has(t));
  if (!post.draft && validSiblingLinks.length < 2) flags.push("LOW-OUTBOUND");
  if (post.broken.length) flags.push(`BROKEN(${post.broken.join(", ")})`);
  if (ledgerRaw && !ledgerRaw.includes(`\`${post.slug}\``)) flags.push("NOT-IN-LEDGER");
  if (post.draft) flags.push("draft");
  if (flags.some((f) => f !== "draft")) problems++;

  lines.push(
    `| \`${post.slug}\` | ${validSiblingLinks.map((s) => `\`${s}\``).join(", ") || "—"} | ${
      post.categoryLinks.join(", ") || "—"
    } | ${inb.length} | ${flags.join(" ") || "OK"} |`
  );
}
lines.push("");
lines.push(problems ? `⚠ ${problems} post(s) flagged.` : "✓ All posts clean.");

const report = lines.join("\n");
console.log(report);

if (process.argv.includes("--write")) {
  const START = "<!-- LINKS-REPORT:START — generated by scripts/blog-links-report.mjs, do not edit -->";
  const END = "<!-- LINKS-REPORT:END -->";
  const startIdx = ledgerRaw.indexOf(START);
  const endIdx = ledgerRaw.indexOf(END);
  if (startIdx === -1 || endIdx === -1) {
    console.error("Ledger markers not found; not writing.");
    process.exit(1);
  }
  const updated =
    ledgerRaw.slice(0, startIdx + START.length) +
    "\n\n## Link graph (generated)\n\n" +
    report +
    "\n\n" +
    ledgerRaw.slice(endIdx);
  fs.writeFileSync(LEDGER, updated);
  console.log("\nLedger link-graph section updated.");
}

process.exit(0);
